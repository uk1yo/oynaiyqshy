function openCell(cell){
  if(cell.classList.contains("used")) return;
  let idx=cell.dataset.index;
  currentPoints=questions[idx].pts;
  currentAnswer=questions[idx].a;
  cell.classList.add("used");
  document.getElementById("questionArea").innerHTML="<b>"+questions[idx].q+"</b>";
  document.getElementById("answerArea").style.display="none";
  showAnswerBtn();
  document.getElementById("modal").style.display="flex";
}

function showAnswerBtn(){
  document.getElementById("buttonsArea").innerHTML='<button class="main-btn" onclick="showAnswer()">Жауапты көру</button>';
}

function showAnswer(){
  document.getElementById("answerArea").innerHTML="<b>Жауабы:</b> "+currentAnswer;
  document.getElementById("answerArea").style.display="block";
  showTeamButtons();
}

function showTeamButtons(){
  let btns="";
  for(let i=1;i<=4;i++){
    btns+='<button class="main-btn" onclick="addPoints('+i+')">Команда '+i+' дұрыс</button>';
  }
  document.getElementById("buttonsArea").innerHTML=btns;
}

function addPoints(team){
  scores[team-1]+=currentPoints;
  document.getElementById("t"+team).innerText=scores[team-1];
  closeModal();
}

function closeModal(){document.getElementById("modal").style.display="none";}